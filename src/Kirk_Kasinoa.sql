DELIMITER //
CREATE DEFINER=`root`@`localhost` FUNCTION `total_games_table`(table_id VARCHAR(10)) RETURNS int
    DETERMINISTIC
BEGIN
    DECLARE total INT;

    SELECT COUNT(*) INTO total
    FROM PLAYED
    WHERE ID_TABLE = table_id;

    RETURN total;
END //
DELIMITER ;
	
DELIMITER //
CREATE DEFINER=`root`@`localhost` FUNCTION `total_user_balance`() RETURNS decimal(10,0)
    DETERMINISTIC
BEGIN
    DECLARE total DECIMAL;

    SELECT SUM(BALANCE) INTO total
    FROM USERS;

    RETURN total;
END
DELIMITER ; 

DELIMITER //
	CREATE DEFINER=`root`@`localhost` FUNCTION `win_rate`(user_dni VARCHAR(10)) RETURNS decimal(10,0)
    DETERMINISTIC
BEGIN
    DECLARE wins INT;
    DECLARE total INT;

    SELECT COUNT(*) INTO wins
    FROM PLAYED
    WHERE DNI = user_dni AND RESULT = 'WIN';

    SELECT COUNT(*) INTO total
    FROM PLAYED
    WHERE DNI = user_dni;

    IF total = 0 THEN
        RETURN 0;
    END IF;

    RETURN (wins / total) * 100;
END
DELIMITER ;

DELIMITER //
	CREATE DEFINER=`root`@`localhost` FUNCTION `is_adult`(user_dni VARCHAR(10)) RETURNS tinyint(1)
    DETERMINISTIC
BEGIN
    DECLARE age INT;

    SELECT TIMESTAMPDIFF(YEAR, DATE_OF_BIRTH, CURDATE())
    INTO age
    FROM USERS
    WHERE DNI = user_dni;

    RETURN age >= 18;
END
DELIMITER ;


