DROP DATABASE IF EXISTS KIRK_KASINOA;

CREATE DATABASE KIRK_KASINOA;

USE KIRK_KASINOA;

CREATE TABLE USERS (
DNI VARCHAR(10) PRIMARY KEY,
USERNAME VARCHAR(20) NOT NULL,
PASWORD VARCHAR(10) NOT NULL,
DATE_OF_BIRTH DATE,
TIMES_PLAYED INT,
WINS INT, 
LOSSES INT,
MAX_COMBO INT,
TOTAL21S INT,
TOTAL_LOSSES DOUBLE, 
TOTAL_WINS DOUBLE,

BALANCE DECIMAL CHECK (BALANCE BETWEEN 0 AND 5000)
);

CREATE TABLE TABLEE(
ID_TABLE VARCHAR(10) PRIMARY KEY,
GAME_TYPE ENUM ('BLACKJACK','ROULETTE'),
BET_LIMIT DECIMAL CHECK (BET_LIMIT BETWEEN 0 AND 5000)
);

CREATE TABLE DEALER(
ID_DEALER VARCHAR(10) PRIMARY KEY,
DEALER_NAME VARCHAR(20) NOT NULL,
BALANCE DECIMAL CHECK (BALANCE BETWEEN 0 AND 5000)
);

CREATE TABLE PLAYED(
DNI VARCHAR(10),
ID_TABLE VARCHAR(10),
GAME_DATE DATE,
PRIMARY KEY(DNI,ID_TABLE,GAME_DATE),
RESULT ENUM ('WIN','LOSE'),
FOREIGN KEY (DNI) REFERENCES USERS(DNI),
FOREIGN KEY (ID_TABLE) REFERENCES TABLEE(ID_TABLE) 
);

CREATE TABLE HAVE(
ID_TABLE VARCHAR(10),
ID_DEALER VARCHAR(10),
FOREIGN KEY(ID_TABLE) REFERENCES TABLEE(ID_TABLE),
FOREIGN KEY(ID_DEALER) REFERENCES DEALER(ID_DEALER)
);

